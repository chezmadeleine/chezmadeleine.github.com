<li>For stays of three nights or more, a 30% deposit is required.</li>
<li>To pay, please print and fill in this booking form and send it with your deposit to:-  Chez Madeleine, Le Bourg, St Colomb de Lauzun, 47410, France.</li>
<li>Cheques should be made to:- Madeleine Coia.</li>
