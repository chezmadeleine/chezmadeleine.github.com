<h2>TARIFF, BOOKING, TERMS & CONDITIONS</h2>
<h3>OUR TARIFF</h3>
<p>Chambres d'hote is available from 45&euro; per room per night, including a continental breakfast for 2 persons. There are two options:</p>

<h4>OPTION A</h4>
<ul>
<li>The family suite consists of two adjoining rooms.</li>
<li>The master bedroom has a double bed and the adjacent room twin single beds.</li>
<li>The bathroom is en suite and contains both a bath and separate shower.</li>
<li>The tariff for the each room is 45&euro; per night (for two people).</li>
<li>Should you wish to use both rooms, the tariff is 75&euro; per night (up to 4 people).</li>
</ul>

<h4>OPTION B</h4>
<ul>
<li>Twin bedded room with en suite facilities.</li>
<li>Tariff 45&euro; per night (1-2 persons).</li>
</ul>

